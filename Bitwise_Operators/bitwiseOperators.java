class bitwiseOperators {

    public static void main(String[] args) {
        
        // 0001
        // 0010

        int a=2,b=1;

        System.out.println(a|b);

        System.out.println(a&b);

        System.out.println(a^b);

        System.out.println(a<<1);
        System.out.println(a>>1);

        System.out.println(~a);
    }
    
}
